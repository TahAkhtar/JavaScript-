const month = 3
switch (month) {
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("February")
        break;
    case 3:
        console.log("March")
        // if condition matches it will execute everything except default after 3 that's why we use break 
        break;
    case 4:
        console.log("April")
        break;

    default:
        break;
}